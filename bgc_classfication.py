def build_bgc_general_transformer(config):
    """General transformer for BGC vs Non-BGC classification with FFN - Memory Optimized"""
    inputs = Input(shape=(config['input_dim'],))

    # Reshape for sequence processing
    x = Reshape((config['input_dim'], 1))(inputs)

    # Initial projection to reduce dimensionality
    x = Dense(config['embed_dim'], activation='relu')(x)
    x = Dropout(config['dropout_rate'])(x)

    # Apply pooling to reduce sequence length
    if config['pool_size'] > 1:
        x = AveragePooling1D(pool_size=config['pool_size'])(x)

    # Multi-head attention with reduced dimensions
    attn_output = MultiHeadAttention(
        num_heads=config['num_heads'],
        key_dim=config['embed_dim'] // config['num_heads']
    )(x, x)

    # Add & Norm
    x = Add()([x, attn_output])
    x = LayerNormalization()(x)

    # Feed Forward Network (FFN)
    ffn_output = Dense(config['ff_dim'], activation='relu')(x)
    ffn_output = Dropout(config['dropout_rate'])(ffn_output)
    ffn_output = Dense(config['embed_dim'])(ffn_output)

    # Add & Norm
    x = Add()([x, ffn_output])
    x = LayerNormalization()(x)

    # Dual pooling
    avg_pool = GlobalAveragePooling1D()(x)
    max_pool = GlobalMaxPooling1D()(x)
    x_combined = Concatenate(name='feature_layer')([avg_pool, max_pool])
    x_combined = Dropout(config['dropout_rate'])(x_combined)

    # Feature output
    features = Dense(config['output_features'], activation='relu', name='bgc_features')(x_combined)

    # Binary output (BGC vs Non-BGC)
    binary_output = Dense(1, activation='sigmoid', name='bgc_binary')(features)

    model = Model(inputs=inputs, outputs=[features, binary_output],
                  name='bgc_general_transformer')

    model.compile(
        optimizer=tf.keras.optimizers.Adam(learning_rate=config['learning_rate']),
        loss={
            'bgc_features': 'mse',
            'bgc_binary': 'binary_crossentropy'
        },
        loss_weights={'bgc_features': 0.2, 'bgc_binary': 0.8},  # Emphasize classification
        metrics={'bgc_binary': ['accuracy', 'AUC']}
    )

    return model

def build_bgc_class_transformer(config, class_name):
    """Transformer for specific BGC class classification with FFN - Memory Optimized"""
    inputs = Input(shape=(config['input_dim'],))

    # Reshape
    x = Reshape((config['input_dim'], 1))(inputs)

    # Initial projection
    x = Dense(config['embed_dim'], activation='relu')(x)
    x = Dropout(config['dropout_rate'])(x)

    # Apply pooling to reduce sequence length
    if config['pool_size'] > 1:
        x = AveragePooling1D(pool_size=config['pool_size'])(x)

    # Multi-head attention with reduced dimensions
    attn_output = MultiHeadAttention(
        num_heads=config['num_heads'],
        key_dim=config['embed_dim'] // config['num_heads']
    )(x, x)

    # Add & Norm
    x = Add()([x, attn_output])
    x = LayerNormalization()(x)

    # Feed Forward Network (FFN)
    ffn_output = Dense(config['ff_dim'], activation='relu')(x)
    ffn_output = Dropout(config['dropout_rate'])(ffn_output)
    ffn_output = Dense(config['embed_dim'])(ffn_output)

    # Add & Norm
    x = Add()([x, ffn_output])
    x = LayerNormalization()(x)

    # Pooling
    avg_pool = GlobalAveragePooling1D()(x)
    max_pool = GlobalMaxPooling1D()(x)
    x_pooled = Concatenate()([avg_pool, max_pool])
    x_pooled = Dropout(config['dropout_rate'])(x_pooled)

    # Feature output
    features = Dense(config['output_features'], activation='relu',
                     name='class_features')(x_pooled)

    # Binary output for this BGC class
    binary_output = Dense(1, activation='sigmoid', name='class_binary')(features)

    model = Model(inputs=inputs, outputs=[features, binary_output],
                  name=f'bgc_class_transformer_{class_name}')

    model.compile(
        optimizer=tf.keras.optimizers.Adam(learning_rate=config['learning_rate']),
        loss={
            'class_features': 'mse',
            'class_binary': 'binary_crossentropy'
        },
        loss_weights={'class_features': 0.2, 'class_binary': 0.8},
        metrics={'class_binary': ['accuracy', 'AUC']}
    )

    return model

