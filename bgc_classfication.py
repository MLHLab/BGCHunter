    # Evaluate
    actual_epochs = len(history.history['loss'])
    train_binary_acc = history.history['class_binary_accuracy'][-1]
    val_binary_acc = history.history['val_class_binary_accuracy'][-1]

    print(f"   ✓ Training complete: {actual_epochs}/{MAX_EPOCHS} epochs")
    print(f"   ✓ Training accuracy: {train_binary_acc:.4f}")
    print(f"   ✓ Validation accuracy: {val_binary_acc:.4f}")
    print(f"   ✓ Best validation loss: {min(history.history['val_class_binary_loss']):.4f}")
    print(f"   ✓ Model saved: {model_path}")

    # Early stopping statistics
    if actual_epochs < MAX_EPOCHS:
        print(f"   ⏹️  Early stopping triggered at epoch {actual_epochs} (saved {MAX_EPOCHS - actual_epochs} epochs)")

    return model, history

