#!/usr/bin/env python3
# -*- coding: utf-8 -*-

"""
XGBoost + 5-Fold CV + AUROC + Test Evaluation
"""

import os
import numpy as np
import pandas as pd
import joblib
import xgboost as xgb

from sklearn.model_selection import StratifiedKFold
from sklearn.metrics import (
    accuracy_score, precision_score, recall_score,
    f1_score, roc_auc_score
)
from sklearn.preprocessing import label_binarize

# ======================================================
# PATHS
# ======================================================
DATA_DIR = "/home/andromeda_r740/MB/TRAINING_OUTPUT"
MODEL_SAVE = "/home/andromeda_r740/MB/xgboost_final_model.pkl"
OUTPUT_DIR = "/home/andromeda_r740/MB/XGB_RESULTS"

os.makedirs(OUTPUT_DIR, exist_ok=True)

# ======================================================
# LOAD DATA
# ======================================================
print("\nLoading data...")

X_train = np.load(os.path.join(DATA_DIR, "X_train_combined.npy"))
X_val   = np.load(os.path.join(DATA_DIR, "X_val_combined.npy"))
X_test  = np.load(os.path.join(DATA_DIR, "X_test_combined.npy"))

y_train = np.load(os.path.join(DATA_DIR, "y_train.npy"))
y_val   = np.load(os.path.join(DATA_DIR, "y_val.npy"))
y_test  = np.load(os.path.join(DATA_DIR, "y_test.npy"))

# Merge train + val
X = np.vstack([X_train, X_val])
y = np.hstack([y_train, y_val])

num_classes = len(np.unique(y))

# ======================================================
# DEFINE PARAMETERS ONCE ✅
# ======================================================
xgb_params = dict(
    n_estimators=400,
    max_depth=6,
    learning_rate=0.05,
    subsample=0.8,
    colsample_bytree=0.8,
    objective="multi:softprob",
    eval_metric="mlogloss",
    tree_method="hist",
    n_jobs=-1,
    random_state=42,
    num_class=num_classes
)

# ======================================================
# STRATIFIED 5-FOLD CV
# ======================================================
print("\nRunning 5-Fold Stratified CV...")

skf = StratifiedKFold(n_splits=5, shuffle=True, random_state=42)

metrics_list = []

for fold, (train_idx, val_idx) in enumerate(skf.split(X, y), 1):
    print(f"\nFold {fold}...")

    X_tr, X_va = X[train_idx], X[val_idx]
    y_tr, y_va = y[train_idx], y[val_idx]

    model = xgb.XGBClassifier(**xgb_params)

    model.fit(X_tr, y_tr)

    y_pred = model.predict(X_va)
    y_prob = model.predict_proba(X_va)

    y_va_bin = label_binarize(y_va, classes=np.arange(num_classes))

    acc = accuracy_score(y_va, y_pred)
    prec = precision_score(y_va, y_pred, average='weighted')
    rec = recall_score(y_va, y_pred, average='weighted')
    f1 = f1_score(y_va, y_pred, average='weighted')

    try:
        auc = roc_auc_score(y_va_bin, y_prob, multi_class='ovr', average='weighted')
    except:
        auc = np.nan

    metrics_list.append([acc, prec, rec, f1, auc])

    print(f"Accuracy: {acc:.4f}, F1: {f1:.4f}, AUROC: {auc:.4f}")

# ======================================================
# CV RESULTS
# ======================================================
metrics_array = np.array(metrics_list)

mean_metrics = metrics_array.mean(axis=0)
std_metrics = metrics_array.std(axis=0)

cv_results = pd.DataFrame({
    "Metric": ["Accuracy", "Precision", "Recall", "F1-score", "AUROC"],
    "Mean": mean_metrics,
    "Std": std_metrics
})

cv_results.to_csv(os.path.join(OUTPUT_DIR, "cv_results.csv"), index=False)

print("\n📊 Cross-Validation Results:")
print(cv_results)

# ======================================================
# FINAL MODEL (same parameters ✅)
# ======================================================
print("\nTraining final model...")

final_model = xgb.XGBClassifier(**xgb_params)
final_model.fit(X, y)

# ======================================================
# TEST EVALUATION
# ======================================================
print("\nEvaluating on test set...")

y_test_pred = final_model.predict(X_test)
y_test_prob = final_model.predict_proba(X_test)

y_test_bin = label_binarize(y_test, classes=np.arange(num_classes))

test_acc = accuracy_score(y_test, y_test_pred)
test_prec = precision_score(y_test, y_test_pred, average='weighted')
test_rec = recall_score(y_test, y_test_pred, average='weighted')
test_f1 = f1_score(y_test, y_test_pred, average='weighted')

try:
    test_auc = roc_auc_score(y_test_bin, y_test_prob, multi_class='ovr', average='weighted')
except:
    test_auc = np.nan

test_results = pd.DataFrame({
    "Metric": ["Accuracy", "Precision", "Recall", "F1-score", "AUROC"],
    "Value": [test_acc, test_prec, test_rec, test_f1, test_auc]
})

test_results.to_csv(os.path.join(OUTPUT_DIR, "test_results.csv"), index=False)

print("\n📊 Test Results:")
print(test_results)

# ======================================================
# SAVE MODEL
# ======================================================
joblib.dump(final_model, MODEL_SAVE)

print("\n✅ Model saved:", MODEL_SAVE)