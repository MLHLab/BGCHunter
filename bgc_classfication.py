# 4. FEATURE EXTRACTION FUNCTIONS
# ============================
def extract_features(model, X_data, is_class_transformer=False):
    """Extract features from transformer with memory optimization"""
    if is_class_transformer:
        feature_extractor = Model(
            inputs=model.input,
            outputs=model.get_layer('class_features').output
        )
    else:
        feature_extractor = Model(
            inputs=model.input,
            outputs=model.get_layer('bgc_features').output
        )

    # Extract features in smaller batches to avoid memory issues
    batch_size = min(64, len(X_data))
    features = []

    for i in range(0, len(X_data), batch_size):
        batch = X_data[i:i+batch_size]
        batch_features = feature_extractor.predict(batch, verbose=0)
        features.append(batch_features)

    return np.vstack(features)

