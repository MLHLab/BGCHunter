"""
XGBOOST GRID SEARCH (Transformer Features)

"""

import os
import numpy as np
import pandas as pd
import joblib
import xgboost as xgb

from sklearn.model_selection import train_test_split, ParameterGrid
from sklearn.metrics import f1_score, accuracy_score

# ======================================================
# PATHS
# ======================================================
DATA_DIR = "/home/andromeda_r740/MB/TRAINING_OUTPUT_8thJAN"
OUTPUT_DIR = "/home/andromeda_r740/MB/XGB_GRID_SEARCH"
os.makedirs(OUTPUT_DIR, exist_ok=True)

# ======================================================
# SETTINGS (like transformer search)
# ======================================================
SEARCH_SUBSET_RATIO = 0.2
RANDOM_STATE = 42

# ======================================================
# PARAMETER GRID
# ======================================================
XGB_GRID = {
    "n_estimators": [200,400,600],
    "max_depth": [6, 8],
    "learning_rate": [0.05, 0.1],
    "subsample": [0.5,0.8],
    "colsample_bytree": [0.5,0.8]
}

# ======================================================
# LOAD DATA
# ======================================================
print("\n📂 Loading transformer features...")

X = np.load(os.path.join(DATA_DIR, "X_train_combined.npy"))
y = np.load(os.path.join(DATA_DIR, "y_train.npy"))

# ======================================================
# SUBSET (like transformer search)
# ======================================================
print(f"\nUsing {SEARCH_SUBSET_RATIO*100}% data for search...")

X_sub, _, y_sub, _ = train_test_split(
    X, y,
    train_size=SEARCH_SUBSET_RATIO,
    stratify=y,
    random_state=RANDOM_STATE
)

X_train, X_val, y_train, y_val = train_test_split(
    X_sub, y_sub,
    test_size=0.2,
    stratify=y_sub,
    random_state=RANDOM_STATE
)

print("Train:", X_train.shape)
print("Val:", X_val.shape)

# ======================================================
# GRID SEARCH
# ======================================================
print("\n🚀 Running XGBoost Grid Search...")

param_list = list(ParameterGrid(XGB_GRID))

best_score = -1
best_config = None
results = []

for i, params in enumerate(param_list):
    print(f"[{i+1}/{len(param_list)}] Testing: {params}", end="... ")

    try:
        model = xgb.XGBClassifier(
            objective="multi:softprob",
            eval_metric="mlogloss",
            tree_method="hist",
            n_jobs=-1,
            **params
        )

        model.fit(X_train, y_train)

        y_pred = model.predict(X_val)

        acc = accuracy_score(y_val, y_pred)
        f1 = f1_score(y_val, y_pred, average="weighted")

        score = f1  # primary metric

        results.append({
            **params,
            "accuracy": acc,
            "f1_score": f1
        })

        print(f"F1: {f1:.4f}")

        if score > best_score:
            best_score = score
            best_config = params

    except Exception as e:
        print(f"Failed ({e})")

# ======================================================
# SAVE RESULTS
# ======================================================
results_df = pd.DataFrame(results)
results_df.to_csv(os.path.join(OUTPUT_DIR, "xgb_grid_results.csv"), index=False)

pd.DataFrame([best_config]).to_csv(
    os.path.join(OUTPUT_DIR, "best_xgb_config.csv"),
    index=False
)

# ======================================================
# FINAL OUTPUT
# ======================================================
print("\n" + "="*60)
print("🏆 BEST XGBOOST CONFIG")
print("="*60)

print("XGB_CONFIG = {")
for k, v in best_config.items():
    print(f"    '{k}': {v},")
print("}")

print(f"\nBest F1-score: {best_score:.4f}")
print(f"\nSaved results to: {OUTPUT_DIR}")